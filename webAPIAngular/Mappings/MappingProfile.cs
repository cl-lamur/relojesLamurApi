using AutoMapper;
using RelojesLamur.API.DTOs.Auth;
using RelojesLamur.API.DTOs.Orders;
using RelojesLamur.API.DTOs.Products;
using RelojesLamur.API.DTOs.Users;
using RelojesLamur.API.Entities;

namespace RelojesLamur.API.Mappings;

public class MappingProfile : Profile
{
    public MappingProfile()
    {
        // ?? Product ?????????????????????????????????????????
        CreateMap<Product, ProductDto>();
        CreateMap<Product, ProductDetailDto>();
        CreateMap<ProductFeatures, ProductFeaturesDto>();

        // ?? User ????????????????????????????????????????????
        CreateMap<User, UserInfoDto>();
        CreateMap<User, UserDto>();

        // ?? Order ???????????????????????????????????????????
        CreateMap<Order, OrderDto>();
        CreateMap<Order, OrderDetailDto>();
        CreateMap<Order, AdminOrderDto>()
            .ForMember(d => d.UserName,
                o => o.MapFrom(s => s.User != null ? s.User.Name  : "Usuario eliminado"))
            .ForMember(d => d.UserEmail,
                o => o.MapFrom(s => s.User != null ? s.User.Email : string.Empty));

        // ?? OrderItem ???????????????????????????????????????
        CreateMap<OrderItem, OrderItemDetailDto>()
            .ForMember(d => d.ProductName,
                o => o.MapFrom(s => s.Product != null ? s.Product.Name     : "Producto eliminado"))
            .ForMember(d => d.ProductImageUrl,
                o => o.MapFrom(s => s.Product != null ? s.Product.ImageUrl : string.Empty));
    }
}
